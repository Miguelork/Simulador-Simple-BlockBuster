    private void btnLog3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLog3ActionPerformed
        if (!(user == "") && !(user == "Seleccione un usuario")) {
            for (Usuario u : manage.listClient) {
                if (u.getNombre().equals(user)) {
                    selected = u;
                }
            }
            JOptionPane.showMessageDialog(null, "Bienvenido " + selected.getNombre() + " \nSu NÃºmero de Socio es " + selected.getNumSocio(), "Login exitoso", JOptionPane.INFORMATION_MESSAGE);
            manage.selected=this.selected;
            Administrador admin = new Administrador(selected);
            admin.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "No ha seleccionado su usuario \nVerifique e intente nuevamente", "Seleccionar Usuario", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnLog3ActionPerformed

